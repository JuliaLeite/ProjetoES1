    public void readJTable() throws Exception {
        
        DefaultTableModel modelo = (DefaultTableModel) jTUsuarios.getModel();
        modelo.setNumRows(0);
      UsuarioDAO udao = new UsuarioDAO();

        for (Usuario u : udao.listarUsuarios()) {

            modelo.addRow(new Object[]{
                u.getId(),
                u.getNome(),
                u.getNivelAcesso(),
            });

        }

    }
    public void readJTableForDesc(String desc) throws Exception {
        
        DefaultTableModel modelo = (DefaultTableModel) jTUsuarios.getModel();
        modelo.setNumRows(0);
      UsuarioDAO udao = new UsuarioDAO();

        for (Usuario u : udao.readForDesc(desc)) {

            modelo.addRow(new Object[]{
                u.getId(),
                u.getNome(),
                u.getNivelAcesso(),
            });

        }

    }

