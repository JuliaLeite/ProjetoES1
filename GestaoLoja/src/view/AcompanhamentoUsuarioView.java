    private void ButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonExcluirActionPerformed
        // TODO add your handling code here:

        //        System.out.println("Linha selecionada: "+jTUsuarios.getSelectedRow());
        if (jTUsuarios.getSelectedRow() != -1) {

          Usuario u = new Usuario();
          UsuarioDAO udao = new UsuarioDAO();

            u.setId((int) jTUsuarios.getValueAt(jTUsuarios.getSelectedRow(), 0));

            try {
                udao.delete(u);
            } catch (Exception ex) {
                Logger.getLogger(AcompanhamentoUsuarioView.class.getName()).log(Level.SEVERE, null, ex);
            }

            try {
                readJTable();
            } catch (Exception ex) {
                Logger.getLogger(AcompanhamentoUsuarioView.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um para excluir.");
        }

    }//GEN-LAST:event_ButtonExcluirActionPerformed

