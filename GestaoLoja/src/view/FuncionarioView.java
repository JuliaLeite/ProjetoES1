    public void readJTable() throws Exception {
        
        DefaultTableModel modelo = (DefaultTableModel) jTFuncionarios.getModel();
        modelo.setNumRows(0);
      FuncionarioDAO fdao = new FuncionarioDAO();

        for (Funcionario f : fdao.read()) {

            modelo.addRow(new Object[]{
                f.getId(),
                f.getNome(),
                f.getCargo(),
                f.getSalario()
            });

        }

    }
    public void readJTableForDesc(String desc) throws Exception {
        
        DefaultTableModel modelo = (DefaultTableModel) jTFuncionarios.getModel();
        modelo.setNumRows(0);
      FuncionarioDAO fdao = new FuncionarioDAO();

        for (Funcionario f : fdao.readForDesc(desc)) {

            modelo.addRow(new Object[]{
                f.getId(),
                f.getNome(),
                f.getCargo(),
                f.getSalario()
            });

        }

    }

